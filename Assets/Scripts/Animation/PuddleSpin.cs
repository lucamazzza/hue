    // OnStateUpdate is called on each Update frame between OnStateEnter and OnStateExit callbacks
    override public void OnStateUpdate(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        int move = animator.GetInteger("Move");
        float currentSpeed = animator.GetFloat("Puddle Speed");

        if (move > 0) {
            animator.SetFloat("Puddle Speed", Mathf.Lerp(currentSpeed, 1f, 0.01f));
        }
        else
        {
            animator.SetFloat("Puddle Speed", Mathf.Lerp(currentSpeed, 0f, 0.01f));
        }
    }

